/**
 * @file Defines the DayOfWeek enumeration.
 */

/**
 * Enumeration representing the days of the week.
 * Values are stored in Spanish for domain consistency.
 */
export enum DayOfWeek {
  /** Monday */
  MONDAY = "LUNES",

  /** Tuesday */
  TUESDAY = "MARTES",

  /** Wednesday */
  WEDNESDAY = "MIERCOLES",

  /** Thursday */
  THURSDAY = "JUEVES",

  /** Friday */
  FRIDAY = "VIERNES",

  /** Saturday */
  SATURDAY = "SABADO",

  /** Sunday */
  SUNDAY = "DOMINGO",
}
